<h1><a href="http://www.gamesetwatch.com/2008/05/column_the_game_anthropologist_defense_of.php" rel="bookmark">Column: The Game Anthropologist: <i>Defense of the Ancients</i>: An Underground Revolution</a></h1>

<p class="posted">May 30, 2008  4:00 PM | </p>

<div class="postContent">
  <p><img alt="Dota%20heroes.jpg" src="http://www.gamesetwatch.com/Dota%20heroes.jpg"  hspace="5" align="left"/> <i>[The Game Anthropologist chronicles <a href=http://etelmik.blogspot.com>Michael Walbridge</a>'s ventures into gaming communities as he reports on their inhabitants and culture.]</i></p>

<p>If you've played <i>Warcraft III</i> on Battle.net lately you'd feel like more people were playing <a href="http://en.wikipedia.org/wiki/Defense_of_the_Ancients"><i>Defense of the Ancients</i></a>, popularly called <i>DOTA</i>, than the actual Blizzard game it’s based on. In fact, <i>DOTA</i> is likely the most popular and most-discussed free, non-supported game mod in the world, judging by the numbers. (It's also been a notable inspiration for the <a href="http://www.towerdefence.net/">plethora of Tower Defense Flash games</a> in recent years.)</p>

<p>Over at the “official” <i>DOTA Allstars</i> forums as I write this, there are 800 people logged in and over 100,000 total topics and over 23,000 topics in the general forum in the last month. By comparison, <i>Warcraft III</i>, the game it is modded from, only has a few thousand topics at most over on the Battle.net website. </p>

<p><b><u>Competitive RPG Action the Way We Want It </b></u></p>

<p>The game itself is technically played in RTS format but is often described as “RPG combat.” Many players were disappointed by <i>Warcraft III</i>; some were disappointed it wasn’t more like <i>Starcraft</i>, and many found that the heroes system watered the game down into an experiment that was interesting enough to play, but not fun enough to worship. </p>

<p><i>Warcraft III</i> match strategies are centered around the selection, leveling, and gearing of heroes, with all units simply being support for the hero. Turning points, victories, and defeats are hero-centered. <i>DOTA</i> turns <i>Warcraft III</i>’s hero system on its head—instead of playing an army with an important leader, you simply play the important leader while the computer takes care of the army.</p>
  <p>Like any brilliant game, the concept is simple and the strategy is complex: each side has an Ancient and the object of the game is to destroy that ancient. There are three paths from base to base with three defensive towers on the way to that base. At precise and frequent intervals, each base sends a set of computer-controlled creeps towards the enemy base. Players control heroes who receive earn money as time passes and for killing enemy creeps and enemy heroes. </p>

<p>The maximum level is 25 (instead of <i>Warcraft III’s</i> 10) and each team gets 5 heroes. There are over 70 heroes to choose from. At level 1, a hero can barely take on two creeps by himself. At level 25 and with the right items, a hero can wade through a dozen creeps with little to no consequences.</p>

<p>The strategy focuses on leveling, getting hero kills, pushing the enemy’s base with your allied creeps and defending against the same. There are also a large number of items for purchase, some coming from “recipes” that mix multiple items to make single powerful items, a necessity since each hero has only 6 slots. If a player dies, he loses money and valuable time to be leveling while providing a lot of experience to the other player. If he dies frequently, he’s called a “feeder” and his team will usually become venomous.</p>

<p><i>DOTA</i> is no small mod; only <i>Counter-Strike</i> can compare for depth, fun, fan-base, and community depth. <i>DOTA Allstars</i> is frequently updated, tested, and changed. The <a href= http://www.getdota.com/changelog.txt>changelog</a> has a professional quality to it; <i>DOTA</i> is well-balanced to the point that it had its own tournament at Blizzcon in 2005 and it is represented in numerous Esports leagues and other cash prize tournaments. </p>

<p><b><u>A Mod Made by Legends and the People </b></u></p>

<p>The only thing about <i>DOTA</i> that is as fascinating as its gameplay and success is its history and evolution. Its designers and programmers are largely anonymous. The original designer is only known as Eul; he released <i>DOTA</i> even before the expansion <i>Warcraft III: The Frozen Throne</i> came out. After its release, Guinsoo, another anonymous modder, took over and converted it for the expansion. </p>

<p><img alt="Dota%20heroes.jpg" src="http://www.gamesetwatch.com/dota2.jpg"  hspace="5" align="right"/> He later stopped developing for it; the also-anonymous Icefrog took his place. Icefrog responded to an interview request with “I usually prefer to not do interviews” (he hasn’t done one anywhere yet). Players debate the validity of reports of his even being seen playing the game; what little information that is offered about him is only offered by people who claim to know him. </p>

<p>There is no verifiable or documented information about him—the only proof we have that someone named Icefrog is even involved is by his email on getdota.com, his name in the load screen, and the few people who have truly, if unverifiably, interacted with him.</p>

<p>Over at the <i>DOTA Allstars</i> community, the highly loyal players suggest many, many changes and ideas. “Eul, Guinsoo or IceFrog alone did not make the map. The <i>DOTA</i> community…makes the map”, one player said. Loadscreen art is drawn by fans. Some bugs are found and some items and heroes are made, erased, and changed almost entirely because of community outreach. Seven mirrors on <a href="http://www.getdota.com">getdota.com</a> are responsible for its dissemination. Once a new hero, the fairie dragon, was found by the community to be too powerful. Icefrog must have agreed—the fairie dragon was changed in less than two weeks.</p>

<p>But despite all of the community’s help, it is still Icefrog, a man who may be named Jeremy, who may be from Boston and who may study at UCLA, which makes the final changes. The masses may be the power of the movement, but the figurehead and initiator of all that changes is still focused on a mysterious, almost spiritual figurehead.</p>

<p>As curious a figure as Icefrog is, his identity ultimately doesn’t matter to the game’s progress. The community has faith that if one leader leaves, another will take his place. Previous modders Eul and Guinsoo did much for <i>DOTA</i>; they have in-game items named after them. If Icefrog steps down, doubtless another will take his place as the Zorro of Battle.net. </p>

<p><b><u>Give Us the Tools and We’ll Make the Rules </b></u></p>

<p><i>DOTA</i> is played using custom map settings on Battle.net, which is a setting that anyone would have said couldn’t be governed by anyone other than Blizzard. <i>Warcraft III</i> matches are made randomly by Battle.net to prevent abuse of the stats system, which is a way of measuring player skill for practice or clan acceptance. </p>

<p>It also ensures that players are close enough geographically so that disconnecting doesn’t interfere with stats or matches. In a custom map, none of this is available. Anyone can join any game no matter how poor or distant the connections, and there are no stats. A round of <i>DOTA</i> is usually at least half an hour long, and if someone leaves the teams become imbalanced. </p>

<p>It seems impossible to get ten people to stick in an online game for approximately an hour, but the <i>DOTA</i> community has found ways to do this with consistency. One program allows the host to check the country from which all users originate. Some games that are hosted now say something like “DOTA 6.48 –apem BR!”, which means that it’s <i>DOTA</i>, it’s the 6.48 version, it’s the –apem game modes, and it’s only for players in Brazil. Even if you speak Portuguese, you’ll likely be booted. Personal banlists ensure that if someone doesn’t like you or if you leave within 5 minutes, you will be kicked the next time you join that person’s matches.</p>

<p>Team Dota Allstars is a committee dedicated to mature and complete games. TDA has rules, its own forum, channel, certification process (getting on the “safelist”) and banlist. The process to be safelisted, which proves your willingness to not start games you know you can’t finish and to work as a team in what can only be a team game, is highly inconvenient, necessary, and effective. </p>

<p><b><u>The Underground in Public </b></u></p>

<p><i>DOTA</i> is a delight to all who play it: it’s surprisingly addictive and even pastiche, mixing the highs and lows of gaming and gaming culture. <i>DOTA</i>’s quirks, governments, outlaws, and innovation show us that it’s much easier to renovate for the masses when the masses are involved. The vision of one leader alone is required, but never sufficient.</p>

