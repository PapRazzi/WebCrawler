			<h3 style="margin: 0; font-size: 18px;">Game Design Challenge: Reboot!</h3> <small>[02.06.13]</small> <br />
			 <span>- Danny Cowan</span></li>
			<li><p><p>
GameCareerGuide.com&#39;s Game Design Challenge is an exercise in becoming a game developer, asking you to look at games in a new way -- from the perspective of a game creator, producer, marketer, businessperson, and so forth. <br />
</p>
<p>
Every other Wednesday we&#39;ll present you with a challenge about developing video games. You&#39;ll have two weeks to brainstorm a brilliant solution (see below for how to submit your answers). After the two week submission period elapses, the best answers and the names of those who submitted them will be posted. <br />
</p>
<p>
<u><strong>The Challenge</strong></u> <br />
</p>
<p>
Reboot a franchise from the PlayStation 2, GameCube, and Xbox era. <br />
</p>
<p>
<u><strong>Assignment Details</strong></u> <br />
</p>
<p>
Capcom&#39;s Devil May Cry series was in dire straits during this hardware generation. Despite a strong final showing on the PlayStation 2 with Devil May Cry 3, the Xbox 360 and PlayStation 3 follow-up Devil May Cry 4 was criticized for its repetitive gameplay and retreading of well-worn series material. It was obvious that major changes were needed to keep the series from becoming stale. <br />
</p>
<p>
This year, the Devil May Cry franchise was rebooted with the release of <a href="http://en.wikipedia.org/wiki/DmC_(video_game)">DmC: Devil May Cry</a>, a drastically overhauled entry that recast series protagonist Dante as a half-angel social outcast with supernatural powers. DmC was created by a new development team, and while many elements will be familiar to long-time series fans, the reboot is characterized by a noticeable shift in narrative style, along with a number of gameplay tweaks that met with mixed reactions from fans. <br />
</p>
<p align="center">
<img src="/db_area/images/item_images/130205/reboot_wide.jpg" border="1" alt=" " hspace="5" vspace="5" width="580" height="326" /> <br />
</p>
<p>
While time will tell if&nbsp;DmC is judged as a success or a failure overall, it was a necessary step in keeping the series alive and relevant. Here&#39;s where you come in.<br />
</p>
<p>
For Game Career Guide&#39;s latest Game Design Challenge, <strong>your task is to reboot a franchise from the PlayStation 2, GameCube, and Xbox era with a new series entry for current consoles</strong>. <br />
</p>
<p>
The changes you make to your chosen franchise&#39;s formula don&#39;t necessarily have to be as drastic as the approach Capcom took with Devil May Cry, but you should carefully consider what made the series a success to begin with. In your entry, <strong>describe which key franchise elements you would keep, and explain what you would change in order to make it a better experience for modern audiences</strong>. <br />
</p>
<p>
For your entry, you can choose any franchise that saw a release or sequel during the PS2/GC/Xbox era (roughly between the years 2000 and 2008). You may choose to overhaul anything from a major, ongoing franchise to an obscure favorite -- if you want to reboot <a href="http://en.wikipedia.org/wiki/Halo_(series)">Halo</a>, you may do so, or if you&#39;d rather take this opportunity to design that <a href="http://en.wikipedia.org/wiki/Spy_Fiction_(video_game)">Spy Fiction</a> sequel you&#39;ve always wanted, by all means, go for it! <br />
</p>
<p>
<u><strong>To Submit</strong></u> <br />
</p>
<p>
Work on your ideas, figure out your strategy for coming up with a solution, and ask questions on the <a href="http://gamecareerguide.com/forums/">forums</a>. When your submission is complete, send it to gamedesignchallenge@gamecareerguide.com with the subject line &quot;Design Challenge: Reboot!&quot; <strong>Please type your answer directly in the email body -- do not submit .pdf or Word documents.</strong> <br />
</p>
<p>
Submissions should be no more than 500 words and may contain up to three images. Be sure to include your full name and school affiliation or job title. <br />
</p>
<p>
Entries must be submitted by <strong>Wednesday, February 20.</strong> <br />
</p>
<p>
Results will be posted <strong>Tuesday, February 26.</strong> <br />
</p>
<p>
